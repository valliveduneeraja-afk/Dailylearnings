class Arrays {
    public static void main(String[] args){
        int a[] = new int[5];                                       ///Declaration
        a[0] = 10;
        a[1] = 20;
        a[2] = 30;                                                  ///Assigning Values
        a[3] = 40;
        a[4] = 50;
        int b[] = {10, 20, 30, 40, 50};
        System.out.println(a[0] + " " + a[1] + " " + a[2] + " " + a[3] + " " + a[4]);       ///Printing
        System.out.println(b);
    }
}
